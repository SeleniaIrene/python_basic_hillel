#Написати програму, яка розраховує індекс маси тіла якщо користувач може ввести
# значення ваги і зросту у різних системах виміру(по 2 на кожну)

choice = input("Выберите систему измерения: 1 - метрическая, 2 - имперская(футы). Ваш выбор: ")

if choice == "1":
    weight = float(input("Введите массу тела в килограммах: "))
    height = float(input("Введите рост в метрах: "))
    bmi = weight / (height ** 2)
    print("Индекс масы тела:", bmi)
elif choice == "2":
    weight = float(input("Введите массу тела в фунтах: "))
    height = float(input("Введите рост в дюймах: "))
    bmi = (weight * 703) / (height ** 2)
    print("Индекс масы тела:", bmi)
else:
    print("Ошибка")
