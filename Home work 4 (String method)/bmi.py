b = 16
c = 5
result = b // c
remain = b % c
print(f"Ваше количество долларов {result:.2f},Ваша сдача {remain:.2f} грн")

# Між :.2f и наступною строкою немає пробілу після коми
# Також можливо, що помилка пов'язана з версією Python. F-рядки були введені в Python з версії 3.6
# Реалізація в попередніх версіях виглядає так:
print("Ваше количество долларов {:.2f},Ваша сдача {:.2f} грн".format(result, remain))
print("Ваше количество долларов %.2f,Ваша сдача %.2f грн" % (result, remain))

